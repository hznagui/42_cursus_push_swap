char *ft_error(char **argc, int argv)
{
 int i;
 int a;
 a = 0;
 i = 0;
 while(a <= argv)
 {
    while(argc[a][i] != '\0')
    {
        if (is_digit(argc[a][i]))
        {
            return("Error\n");
        }
        i++;        
    }
    i = 0;
    a++;
 }    
}